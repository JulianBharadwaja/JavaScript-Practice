var num1 = 10;
var num2 = 16;
var num3 = 20;

// Addition
console.log(num1 + num2);

// Substraction
console.log(num3 - num2);

// Multiplication
console.log(num1 * num3);

// Division
console.log(num2/num3);

// Modulus
console.log(num2%num3);

// increment - post and pre
console.log(num2++); // print the current value of num2 which is 10 and increase it by one
console.log(num2);
console.log(++num2);
console.log(num2);

// decrement
console.log(num2--);
console.log(num2);