var currentDay = "Saturday";

switch(currentDay) {
    case "Monday": console.log('Timings: 10:00-06:00');break;
    case "Tuesday": console.log('Timings: 11:00-06:00');break;
    case "Wednesday": console.log('Timings: 12:00-06:00');break;
    case "Thursday": console.log('Timings: 9:00-06:00');break;
    case "Friday": console.log('Timings: 8:00-06:00');break;
    case "Saturday": console.log('Timings: 6:00-06:00');break;
    case "Sunday": console.log('Timings: 7:00-06:00');break;
    default: console.log("wrong date");break;
}